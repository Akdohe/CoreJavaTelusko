package Loops;
import java.util.Scanner;

public class DoWhileLoop{
    public static void main(String [] args){
        //Even if Condition is Falsed the use Do while loops

        int i=5;
        //will run the block atleast once 
        do{
            System.out.println("Hi " + i);
            i++;
        }while(i<=4); //here condition is flased 
 

 
        
    }
}